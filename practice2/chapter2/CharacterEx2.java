package chapter2;

public class CharacterEx2 {

    public static void main(String[] args) {

        // 문자를 사용할 때는 '' -> 작은따옴표를 사용
        // 문자열을 사용할 때는 "" -> 큰따옴표 사용

        char ch1 = '한';
        char ch2 = '\uD55C'; // 유니코드 값

        System.out.println(ch1);
        System.out.println(ch2);
    }
}
